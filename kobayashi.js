const out = text => process.stdout.write(text);

(new Promise(resolve => {
    let index = 0;
    const array = 'MaidDragon';
    const interval = setInterval(() => {
        out(array[index % array.length]);
        index++;
        if (index >= 600) {
            clearInterval(interval);
            console.log('\n\n\n\n\n');
            resolve();
        }
    }, 15);
})).then(() => {
    const shift = 10;
    let count = 0;
    let strings = new Array(aa.length);
    
    const interval = setInterval(() => {
        for (let i = 0; i < aa.length; i++) {
            strings[i] ??= [];
            for (let j = 0; j < aa[i].length; j++) {
                strings[i][j + shift * count ] ??= ' ';
                if (aa[i][j] !== '\u00a0') {
                    strings[i][j + shift * count ] = aa[i][j];
                }
            }
        }
        out(strings.map(v => v.join('')).join('\n'));
        out(`\x1b[${aa.length - 1}F`);
        count++;
        if (count > 20) {
            clearInterval(interval);
            out('\n'.repeat(aa.length));
        }
    }, 75);
});